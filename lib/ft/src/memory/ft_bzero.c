#include <libft.h>

void		ft_bzero(void *mem, size_t l)
{
	size_t	i;

	i = 0;
	while (l - i >= 8)
	{
		*(uint64_t*)(mem + i) = 0;
		i += 8;
	}
	while (i < l)
		*(char*)(mem + i++) = 0;
}
