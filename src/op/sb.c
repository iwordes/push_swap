#include <push_swap.h>

#define B1 b->arr[b->len - 1]
#define B2 b->arr[b->len - 2]

void	op_sb(t_stack *a, t_stack *b)
{
	int	tmp;

	tmp = B1;
	B1 = B2;
	B2 = tmp;
	(void)a;
}
