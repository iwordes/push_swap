#include <push_swap.h>

#define S s->arr
#define S1 S[s->len - 1]

void	op__rrot(t_stack *s)
{
	UINT	i;
	int		tmp;

	tmp = S[0];
	i = s->len - 1;
	while (i)
	{
		S[i - 1] = S[i];
		i -= 1;
	}
	S1 = tmp;
}
